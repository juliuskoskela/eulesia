async function seedMunicipalities() {
  console.log(
    `\n🏙️  Seeding ${FINNISH_MUNICIPALITIES.length} Finnish municipalities...\n`,
  );

  let added = 0;
  let skipped = 0;

  for (const m of FINNISH_MUNICIPALITIES) {
    const existing = await db
      .select({ id: locations.id })
      .from(locations)
      .where(and(eq(locations.nameFi, m.nameFi), eq(locations.country, "FI")))
      .limit(1);

    if (existing.length > 0) {
      skipped++;
      continue;
    }

    await db.insert(locations).values({
      name: m.name,
      nameLocal: m.nameFi,
      nameFi: m.nameFi,
      nameSv: m.nameSv ?? undefined,
      type: "municipality",
      adminLevel: 7,
      country: "FI",
      population: m.population,
      latitude: m.latitude,
      longitude: m.longitude,
      status: "active",
      contentCount: 0,
    });

    added++;
    process.stdout.write(`  ✓ ${m.name}\n`);
  }

  console.log(`\n✅ Done! Added ${added}, skipped ${skipped} existing.\n`);
}

seedMunicipalities()
  .catch(console.error)
  .finally(() => process.exit());
